/**
 * Removes EXXCESS user role.
 */
function xmldb_local_eexcess_uninstall() {
    global $DB;
    $rolerecord = $DB->get_record('role', array("shortname" => 'eexcessuser'), $fields = '*');
    delete_role($rolerecord->id);
}